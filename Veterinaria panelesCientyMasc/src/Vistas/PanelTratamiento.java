    private void jBNuevoTratamientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBNuevoTratamientoActionPerformed

        if (jTextTipo.getText().isEmpty() || jTextDescripcion.getText().isEmpty() || jTextPrecio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Los campos obligatorios no pueden estar vacíos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            String Tipo = jTextTipo.getText();
            double Precio = Double.parseDouble(jTextPrecio.getText());
            String Descripcion = jTextDescripcion.getText();
            boolean estado = jRadioEstado.isSelected();

            if (verficacion(Tipo)) {
                JOptionPane.showMessageDialog(this, "El tipo contiene números");
            } else {
                Tratamiento trata = new Tratamiento(
                        Tipo,
                        Descripcion,
                        Precio,
                        estado
                );
                TratamientoData.cargarTratamiento(trata);
                JOptionPane.showMessageDialog(this, "Tratamiento Cargado con exito");
                jTextTipo.setText("");
                jTextDescripcion.setText("");
                jTextPrecio.setText("");
                jRadioEstado.setEnabled(false);
                jComboBuscar.removeAllItems();
                cargacombotratamiento();
            }
        }
    }//GEN-LAST:event_jBNuevoTratamientoActionPerformed

