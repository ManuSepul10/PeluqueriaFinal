    private void cargacombotratamiento() {
        ArrayList<Tratamiento> tratamientos = new ArrayList<>();
        tratamientos = TratamientoData.listarTodosTratamiento();
        jComboBuscar.addItem(null);
        for (Tratamiento tratamiento : tratamientos) {
            jComboBuscar.addItem(tratamiento);
        }

    }

    public boolean verficacion(String tipo) {

        int tipo1 = tipo.length();
        boolean bandera = false;
        boolean bandera2 = false;

        for (int i = 0; i < tipo1; i++) {
            bandera2 = false;
            if (Character.isDigit(tipo.charAt(i))) {

                bandera2 = true;
                break;
            }

        }
        if (bandera2 == false) {
            bandera = false;
        } else {
            bandera = true;
        }
        return bandera;

    }
