    private void SeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionarActionPerformed
        if (jComboBuscar.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "debe seleccionar un tratamiento");
        } else {
            trat = (Tratamiento) jComboBuscar.getSelectedItem();
            System.out.println(trat.toString());
            jTextTipo2.setText(trat.getTipo());
            jTextDes2.setText(trat.getDescripcion());
            jTextPreci2.setText(trat.getPrecio() + "");
            jRadio2.setSelected(trat.isEstado());
        }


    }//GEN-LAST:event_SeleccionarActionPerformed

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        if (trat == null) {
            JOptionPane.showMessageDialog(this, "debe seleccionar un tratamiento");
        } else {
            if (jTextTipo2.getText().isEmpty() || jTextDes2.getText().isEmpty() || jTextPreci2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "debe completar todos los campos");
            } else {
                trat.setTipo(jTextTipo2.getText());
                trat.setDescripcion(jTextDes2.getText());
                trat.setPrecio(Double.parseDouble(jTextPreci2.getText()));
                trat.setEstado(jRadio2.isSelected());

                if (verficacion(trat.getTipo())) {
                    JOptionPane.showMessageDialog(this, "El tipo contiene números");
                } else {
                    TratamientoData.modificarTratamiento(trat);
                    JOptionPane.showMessageDialog(this, "se modifico el tratamiento");
                }
            }
        }

    }//GEN-LAST:event_ModificarActionPerformed

