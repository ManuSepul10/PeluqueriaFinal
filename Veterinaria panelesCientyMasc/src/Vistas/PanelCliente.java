    private void jBBuscarApellidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarApellidoActionPerformed
        if (jTApellido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "debe ingresar un Apellido");
        } else {
            jBCargar.setEnabled(false);

            String apellidoBuscado = jTApellido.getText();
            if (verificarSiTieneNumero(apellidoBuscado) == true) {
                JOptionPane.showMessageDialog(this, "Su apellido contiene números");

            } else {

                ArrayList<Cliente> apellidosEncontrados = buscarClientesPorApellido(apellidoBuscado);

                DefaultTableModel modeloTabla = (DefaultTableModel) jTablaClientes.getModel();
                modeloTabla.setRowCount(0);

                for (Cliente cliente : apellidosEncontrados) {

                    modeloTabla.addRow(new Object[]{
                        cliente.getIdCliente(),
                        cliente.getNombre(),
                        cliente.getDocumento(),
                        cliente.getApellido(),
                        cliente.getDireccion(),
                        cliente.getTelefono(),
                        cliente.getPersonaAlternativa()
                    });
                }
            }
        }
    }//GEN-LAST:event_jBBuscarApellidoActionPerformed


    private void jBModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarClienteActionPerformed
        int filaSeleccionada = jTablaClientes.getSelectedRow();
        if (filaSeleccionada != -1) {
            int idCliente = Integer.parseInt(jTablaClientes.getValueAt(filaSeleccionada, 0).toString());

            int nuevoDocumento = Integer.parseInt(jTDocumento.getText());
            String nuevoNombre = jTnombre.getText();
            String nuevoApellido = jTApellido.getText();
            String nuevaDireccion = jTDireccion.getText();
            String nuevoTelefono = jTTelefono.getText();
            String nuevoContacto = jTContacto.getText();
            if (jTnombre.getText().isEmpty() || jTApellido.getText().isEmpty() || jTDireccion.getText().isEmpty() || jTTelefono.getText().isEmpty() || jTContacto.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "debe completar todos los campos");
            } else {
                if (verificarSiTieneNumero(nuevoNombre) == true || verificarSiTieneNumero(nuevoApellido) == true) {
                    JOptionPane.showMessageDialog(this, "Su nombre o apellido contiene números");

                } else {
                    Cliente clienteModificado = new Cliente(nuevoDocumento, nuevoNombre, nuevoApellido, nuevaDireccion, nuevoTelefono, nuevoContacto, idCliente);

                    ClienteData.modificarCliente(clienteModificado);

                    JOptionPane.showMessageDialog(null, "Cliente modificado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                    DefaultTableModel modelo = (DefaultTableModel) jTablaClientes.getModel();
                    modelo.setRowCount(0);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente.");
        }

    }//GEN-LAST:event_jBModificarClienteActionPerformed


