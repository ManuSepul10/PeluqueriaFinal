private boolean verificarSiTieneNumero(String valor) {

        int largoValor = valor.length();

        boolean bandera1 = false;

        for (int i = 0; i < largoValor; i++) {
            bandera1 = false;
            if (Character.isDigit(valor.charAt(i))) {

                bandera1 = true;
                break;
            }

        }
        return bandera1;
    }
